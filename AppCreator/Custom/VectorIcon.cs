		public VectorIcon() {
			Size = 16;
			Color = "000000";
			Icon = "fa-times";
		}

		protected void OnSizeChanged() {
			HeightRequest = Size;
			WidthRequest = Size;

			RecreateIcon();
		}

		protected void OnIconChanged() {
			if (string.IsNullOrEmpty(Icon))
				return;
			
			if (!Icons.Contains(Icon))
				throw new ArgumentOutOfRangeException("Icon", "Invalid icon:" + Color);
			
			RecreateIcon();
		}

		protected void OnColorChanged() {
			if (string.IsNullOrEmpty(Color))
				return;

			int outVal;

			if (Color.Length != 6 || !int.TryParse(Color, NumberStyles.HexNumber, new CultureInfo("en-US"), out outVal))
				throw new ArgumentOutOfRangeException("Color", "Invalid hex color: " + Color);

			RecreateIcon();
		}

		protected void RecreateIcon() {
			if (Instances.Connectivity.IsConnected) {
				Source = new UriImageSource { Uri = new Uri(string.Format("https://appcreator-api.azurewebsites.net/Icons/Generate/{0}/{1}/{2}/true",
					                                                         Icon, 
					                                                         Color,
					                                                         (int) Size))
				};
			} else {
			}
		}
